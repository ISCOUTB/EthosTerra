package org.wpsim.Person.Data;

/**
 * Biological sex of an individual Person agent.
 * Used together with {@link EtapaVida} to determine default social roles
 * and demographic behavior (reproduction, mortality rates).
 *
 * @author jairo
 */
public enum Sexo {

    /** Male individual. */
    MASCULINO,

    /** Female individual. */
    FEMENINO
}
