class ExtraterrestrialRadiation:
    @staticmethod
    def get_ra(latitude: float, month: int, hemisphere: Hemisphere = Hemisphere.NORTHERN) -> float:
        lat_int = int(round(abs(latitude) / 5) * 5)
        lat_int = min(max(lat_int, 0), 70)
        vals = _RA_TABLE.get(lat_int, _RA_TABLE[0])
        idx = month - 1
        if hemisphere == Hemisphere.SOUTHERN:
            idx = (idx + 6) % 12
        return vals[idx]
